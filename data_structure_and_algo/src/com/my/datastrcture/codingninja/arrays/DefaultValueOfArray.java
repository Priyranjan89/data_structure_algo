package com.my.datastrcture.codingninja.arrays;

public class DefaultValueOfArray {
    public static void main(String[] args) {
        int a[] = new int[5];
        byte b[] = new byte[5];
        short c[] = new short[5];
        long d[] = new long[5];
        float e[] = new float[5];
        double f[] = new double[5];
        boolean i[] = new boolean[5];
        char g[] = new char[5];
        String h[] = new String[5];

        System.out.println("Default value of int : "+a[0]);
        System.out.println("Default value of byte : "+b[0]);
        System.out.println("Default value of short : "+c[0]);
        System.out.println("Default value of long : "+d[0]);
        System.out.println("Default value of float : "+f[0]);
        System.out.println("Default value of double : "+f[0]);
        System.out.println("Default value of boolean : "+i[0]);
        System.out.println("Default value of char : "+g[0]);
        System.out.println("Default value of String : "+h[0]);
    }
}
